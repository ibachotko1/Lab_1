using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Warehouse.Front.Services
{
    public static class HelpService
    {
        public static string GetHelp(string pageName)
        {
            if (pageName == null)
                return "Справка не найдена для данной страницы.";

            switch (pageName.ToLower())
            {
                case "main":
                    return GetMainHelp();
                case "products":
                    return GetProductsHelp();
                case "receive":
                    return GetReceiveHelp();
                case "writeoff":
                    return GetWriteOffHelp();
                case "inventory":
                    return GetInventoryHelp();
                case "operations":
                    return GetOperationsHelp();
                default:
                    return "Справка не найдена для данной страницы.";
            }
        }

        private static string GetMainHelp()
        {
            return @"СИСТЕМА УПРАВЛЕНИЯ СКЛАДОМ

Основные функции:
• Товары - просмотр всех товаров на складе
• Приемка - добавление новых товаров на склад
• Списание - списание товаров со склада
• Инвентаризация - корректировка остатков
• Операции - журнал всех операций
• Справка - документация по системе

Для навигации используйте кнопки в верхней панели.

ПОСТУСЛОВИЯ СИСТЕМЫ:
• Все операции фиксируются в журнале
• Общая стоимость инвентаря пересчитывается после каждой операции
• Целостность данных гарантируется проверками предусловий";
        }

        private static string GetProductsHelp()
        {
            return @"ПРОСМОТР ТОВАРОВ

На этой странице отображается полный список товаров на складе.

Информация по колонкам:
• SKU - уникальный идентификатор товара
• Название - наименование товара
• Количество - текущий остаток на складе
• Цена за ед. - стоимость единицы товара
• Поставщик - компания-поставщик
• Дата поставки - дата последней поставки

Данные обновляются автоматически при каждой операции.

ПОСТУСЛОВИЯ:
• Отображается актуальное состояние склада
• Список синхронизирован с репозиторием данных
• Общая стоимость рассчитывается на основе текущих данных";
        }

        private static string GetReceiveHelp()
        {
            return @"ПРИЕМКА ТОВАРА

Добавление нового товара на склад:

ПРЕДУСЛОВИЯ:
• SKU должен быть уникальным
• Название не может быть пустым
• Количество > 0
• Цена > 0
• Поставщик не может быть пустым
• Дата поставки не может быть в будущем

ПОСТУСЛОВИЯ:
• Создается новая запись товара
• Количество устанавливается корректно
• Цена за единицу сохраняется
• Добавляется операция типа 'Receive' в журнал
• Общая стоимость инвентаря пересчитывается
• Все изменения фиксируются в репозитории";
        }

        private static string GetWriteOffHelp()
        {
            return @"СПИСАНИЕ ТОВАРА

Списание товара со склада:

ПРЕДУСЛОВИЯ:
• Товар должен существовать (проверка по SKU)
• Количество > 0 и ≤ доступного остатка
• Причина не может быть пустой
• Дата списания не может быть в будущем

ПОСТУСЛОВИЯ:
• Количество товара уменьшается на указанное значение
• Добавляется операция типа 'WriteOff' в журнал
• Общая стоимость инвентаря уменьшается
• Целостность данных сохраняется
• Все изменения фиксируются в репозитории";
        }

        private static string GetInventoryHelp()
        {
            return @"ИНВЕНТАРИЗАЦИЯ

Корректировка расхождений в учете:

ПРЕДУСЛОВИЯ:
• Товар должен существовать (проверка по SKU)
• Фактическое количество ≥ 0
• Причина не может быть пустой
• Дата корректировки не может быть в будущем

ПОСТУСЛОВИЯ:
• Количество товара обновляется до фактического
• Рассчитывается и отображается разница
• Добавляется операция типа 'InventoryAdjustment' в журнал
• Общая стоимость инвентаря пересчитывается
• Причина корректировки сохраняется с указанием разницы";
        }

        private static string GetOperationsHelp()
        {
            return @"ЖУРНАЛ ОПЕРАЦИЙ

Просмотр истории всех операций:

Типы операций:
• Receive - приемка товара
• WriteOff - списание товара  
• InventoryAdjustment - корректировка инвентаря

Информация по операциям:
• Тип операции
• SKU товара
• Количество
• Цена за единицу
• Причина операции
• Дата операции
• Дата записи в журнал

Фильтрация:
• Используйте поле 'Фильтр по SKU' для поиска операций по конкретному товару
• Кнопка 'Применить' - фильтрует по введенному SKU
• Кнопка 'Сброс' - отменяет фильтр и показывает все операции
• Кнопка 'Обновить' - загружает актуальные данные из репозитория

ПОСТУСЛОВИЯ:
• Отображается полная история операций
• Данные сортируются по дате записи (новые сверху)
• Фильтрация работает без изменения исходных данных";
        }
    }
}